    private void listarDoctores() {
    // Obtener el modelo de la tabla y limpiar cualquier fila existente
    DefaultTableModel modelo = (DefaultTableModel) tblDatos1.getModel();
    modelo.setRowCount(0); // Limpiar la tabla antes de listar

    // Crear la conexión
    ConexionMySQL conexionMySQL = new ConexionMySQL();
    Connection cn = null;
    CallableStatement cts = null;
    ResultSet rs = null;

    try {
        // Abrir conexión y preparar la llamada al procedimiento almacenado
        cn = conexionMySQL.conexion();
        cts = cn.prepareCall("CALL listar_doctor()");
        rs = cts.executeQuery();

        // Recorrer el ResultSet y agregar las filas a la tabla
        while (rs.next()) {
            Object[] fila = new Object[7]; // Definir el arreglo de columnas
            fila[0] = rs.getInt("id_doc");          // Columna 1: ID del doctor
            fila[1] = rs.getString("nomape");       // Columna 2: Nombre y Apellidos
            fila[2] = rs.getString("dni");          // Columna 3: DNI
            fila[3] = rs.getString("telefono");     // Columna 4: Teléfono
            fila[4] = rs.getString("email");        // Columna 5: Email
            fila[5] = rs.getString("especialidad"); // Columna 6: Especialidad
            fila[6] = rs.getTimestamp("fechap");    // Columna 7: Fecha de Registro

            modelo.addRow(fila); // Agregar la fila al modelo de la tabla
        }
    } catch (SQLException e) {
        System.out.println("Error al listar doctores: " + e.getMessage());
    } finally {
        try {
            if (rs != null) rs.close();
            if (cts != null) cts.close();
            if (cn != null) cn.close();
        } catch (SQLException e) {
            System.out.println("Error al cerrar conexión: " + e.getMessage());
        }
    }
}
    
    

    
