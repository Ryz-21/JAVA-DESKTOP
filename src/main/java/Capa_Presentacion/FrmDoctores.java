    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
 String nomApe = txtNomApe1.getText();
    String dni = txtDNI1.getText();
    String telefono = txtTelefono1.getText();
    String email = txtEmail1.getText();
    String especialidad = cboEspecialidad.getSelectedItem().toString();
    Timestamp fechaRegistro = new Timestamp(new Date().getTime());

    // Crear el objeto Doctor
    Doctor doctor = new Doctor(0, nomApe, dni, telefono, email, especialidad, fechaRegistro);
    DoctorDAO doctorDAO = new DoctorDAO();

    // Registrar doctor y mostrar mensaje
    String mensaje = doctorDAO.registrarDoctor(con, doctor);
    JOptionPane.showMessageDialog(this, mensaje);

    // Listar doctores nuevamente para actualizar la tabla
    listarDoctores();

    // Limpiar campos
    }//GEN-LAST:event_btnAgregarActionPerformed

